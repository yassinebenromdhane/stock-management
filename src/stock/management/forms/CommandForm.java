    void loadTable(){
        model = new DefaultTableModel();
        tblOrders.setModel(model);
        model.addColumn("Id");
        model.addColumn("Ref");
        model.addColumn("Client Id");
        model.addColumn("Product Id");
        model.addColumn("QTE");
        model.addColumn("Confirmed");
        con = new DbConnect().getCon();
        try{
            PreparedStatement ps = con.prepareStatement("select * from orders");
            ResultSet rs = ps.executeQuery();
            while (rs.next()) {
		Order o=new Order(rs.getInt(1), rs.getString(2),rs.getInt(3), rs.getInt(4),rs.getInt(5),rs.getBoolean(6));
                model.addRow(new Object[]{
                    o.getId(),
                    o.getRef(),
                    o.getClient_id(),
                    o.getProduct_id(),
                    o.getQte(),
                    o.isConfirmed()
               
                    
                });
            }
            con.close();
        
        }catch(SQLException e){
            e.printStackTrace();
        }
        
    }
     void loadCustomers(){
        con = new DbConnect().getCon();
        try{
            PreparedStatement ps = con.prepareStatement("select * from customers");
            ResultSet rs = ps.executeQuery();
            while (rs.next()) {
		Customer c=new Customer(rs.getInt(1), rs.getString(2),rs.getString(3),rs.getString(4),rs.getString(5));
                cb_customer.addItem(c.getId()+"-"+c.getName()+" "+c.getLastName());
               
                
               
            }
            con.close();
        
        }catch(SQLException e){
            e.printStackTrace();
        }
       
        
    }
      void loadProducts(){
        con = new DbConnect().getCon();
        try{
            PreparedStatement ps = con.prepareStatement("select * from products");
            ResultSet rs = ps.executeQuery();
            while (rs.next()) {
		Product c=new Product(rs.getInt(1), rs.getString(2),rs.getString(3),rs.getInt(4),rs.getDouble(5),rs.getInt(6));
                cb_product.addItem(c.getId()+"-"+c.getTitle());
               
                
               
            }
            con.close();
        
        }catch(SQLException e){
            e.printStackTrace();
        }
       
        
    }

