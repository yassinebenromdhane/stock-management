    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
            String client_id = cb_customer.getSelectedItem().toString();
            String[] tabCl=client_id.split("-",2);
            int cid = Integer.parseInt(tabCl[0]);
            String product_id = cb_product.getSelectedItem().toString();
            String[] tabPr=client_id.split("-",2);
            int pid = Integer.parseInt(tabPr[0]);
            int qte =Integer.parseInt(qte_field.getText());
            Order o = new Order(0,"",cid,pid,qte,false);
            OrderDAOImp imp = new OrderDAOImp();
            int ins = imp.createOrder(o);
            if(ins!=-1){
                JOptionPane.showMessageDialog(this,"inserted Order");
                this.hide();
                new CommandForm().setVisible(true);
            }else{
                JOptionPane.showMessageDialog(this,"Error insert");
            }
            
            
            
        // TODO add your handling code here:
    }//GEN-LAST:event_jButton1ActionPerformed

